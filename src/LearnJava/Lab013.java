package LearnJava;

public class Lab013 {
    public static void main(String[] args) {
        // If true do this else do that (Ternary operator)
        int result = 30 > 40 ? 23 : 67;
        System.out.println(result);

        String abc = 50 < 30 ? "1" : "TWO";
        System.out.println(abc);
    }
}
