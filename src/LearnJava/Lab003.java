package LearnJava;

public class Lab003 {
    public static void main(String[] args) {
        double one = 34;
        double two = 23;
        double sum = one+two ;
        double sub = one-two ;
        double mul = one*two ;
        double div = one/two ;
        System.out.println("The sum value of the two values is " + sum);
        System.out.println("The sub value of the two values is " + sub);
        System.out.println("The mul value of the two values is " + mul);
        System.out.println("The div value of the two values is " + div);
    }
}